# برش زدن بخشی از فهرست
trucks = ['scania', 'volvo', 'benz', 'leyland', 'renault']
print(trucks[0:3]) #نقطه شروع از خانه صفرم میباشد یعنی از اولین خونه شروع کن نشون بده تا 3 تای اول رو نشون بده
print(trucks[1:4]) # از خونه دوم نشون میده تا خونه چهارم یعنی سه تا خونه رو نشون میده
print(trucks[:4]) # این میاد اوتوماتیک چهارتای اول رو نشون میده

print(trucks[:2])

# show from last index
print(trucks[-3:])